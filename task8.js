/*Дано число n = 1000. Ділить його на 2 стільки разів, поки результат поділу не стане меншим за 50. Яке число вийде?
 Порахуйте кількість ітерацій, необхідних цього (ітерація - це прохід циклу), і запишіть їх у змінну num.*/
let n=1000;
let num=0;
let str=n.toString();
for(let i=0;i<str.length;i++){
    while(n>=50){
       n=n/2;
       num++;
}
}
console.log(n);
console.log(num);